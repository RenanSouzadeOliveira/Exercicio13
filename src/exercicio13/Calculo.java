package exercicio13;

public class Calculo {
	public double area(double ladoa, double ladob){
		double total = 0;
		total = ladoa * ladob;
		return total;
	}
	
	public double perimetro(double ladoa, double ladob){
		double total = 0;
		total = (2*ladoa) + (2*ladob);
		return total;
	}
}
